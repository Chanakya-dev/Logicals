package Strings;

public class Arrays {
    public static void main(String[] args) {
        int[]a={2,3,4};
        for(int h:a){
            System.out.print(h+",");
        }
        System.out.println(a[2]);
        System.out.println();
      
      for(int i=0;i<=a.length-1;i++){
        a[2]=6;
        a[4]=5;
        }
      
    }
}
